package fr.silvharm.commulade.model.pojo;


public class ConfigContainer {
	
	private String secteurPhotoPath, secteurPhotoSave, sitePhotoPath, sitePhotoSave;
	
	
	public ConfigContainer() {
		
	}
	
	
	/********************************
	 * Getters & Setters
	 *******************************/
	
	/**
	 * @return the secteurPhotoPath
	 */
	public String getSecteurPhotoPath() {
		return secteurPhotoPath;
	}
	
	
	/**
	 * @param secteurPhotoPath
	 *           the secteurPhotoPath to set
	 */
	public void setSecteurPhotoPath(String secteurPhotoPath) {
		this.secteurPhotoPath = secteurPhotoPath;
	}
	
	
	/**
	 * @return the secteurPhotoSave
	 */
	public String getSecteurPhotoSave() {
		return secteurPhotoSave;
	}
	
	
	/**
	 * @param secteurPhotoSave
	 *           the secteurPhotoSave to set
	 */
	public void setSecteurPhotoSave(String secteurPhotoSave) {
		this.secteurPhotoSave = secteurPhotoSave;
	}
	
	
	/**
	 * @return the sitePhotoPath
	 */
	public String getSitePhotoPath() {
		return sitePhotoPath;
	}
	
	
	/**
	 * @param sitePhotoPath
	 *           the sitePhotoPath to set
	 */
	public void setSitePhotoPath(String sitePhotoPath) {
		this.sitePhotoPath = sitePhotoPath;
	}
	
	
	/**
	 * @return the sitePhotoSave
	 */
	public String getSitePhotoSave() {
		return sitePhotoSave;
	}
	
	
	/**
	 * @param sitePhotoSave
	 *           the sitePhotoSave to set
	 */
	public void setSitePhotoSave(String sitePhotoSave) {
		this.sitePhotoSave = sitePhotoSave;
	}
	
}
